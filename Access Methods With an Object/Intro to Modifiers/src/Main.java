public class Main {
    /*public is an access modifier, meaning that it is used to set the access level for classes,
    *attributes, method and constructors
    *Access Modifiers - controls thr access level
    *Non-Access Modifiers -do not control access level, but provides other functionality
    *For classes you can use either public or default
    *For attributes, methods and constructors, you can use ,public, private, default, and protected
    *In Non-Access modifiers for classes you can use either final or absract
    *For attributes and methods, you can use ,final, static,abstract, volatile etc*/

    //PUBLIC AS AN ACCESS MODIFIER
    public static void main(String[] args)  {
        System.out.println("Hello world!");
        //The class is accessible by any other class
    }
}